    private void jbtn_musteriSilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_musteriSilActionPerformed
        // TODO add your handling code here:
        if (jtablemusterilistesi.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(rootPane, "Silceğiniz Müşteriyi Seçiniz!");
            return;
        }
        String id = String.valueOf(jtablemusterilistesi.getValueAt(jtablemusterilistesi.getSelectedRow(), 0));
        
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("BP2_1521221051_FeyzaNurSAKA_Odev2PU");
        EntityManager em = emf.createEntityManager();
        
        dtmmusterilistesi.removeRow(jtablemusterilistesi.getSelectedRow());
        
        
        Query q = em.createQuery("DELETE FROM Musteri m  WHERE m.musteriid=:id");
        Query q2 = em.createQuery("DELETE FROM Kayit k  WHERE k.musteriid=:id");

        q2.setParameter("id", Integer.parseInt(id));
        jComboBoxmusteriid.removeItem(Integer.parseInt(id));
        q.setParameter("id", Integer.parseInt(id));

        em.getTransaction().begin();
        q.executeUpdate();
        q2.executeUpdate();
        em.getTransaction().commit();
        em.close();
        emf.close();
        JOptionPane.showMessageDialog(rootPane, "Silme İşlemi Başarılı!");
        temizle();
        checkbox.setSelected(false);

    }//GEN-LAST:event_jbtn_musteriSilActionPerformed
    void temizle() {
        jtxt_musteriAdi.setText("");
        jtxt_musteriSoyadi.setText("");
    }
    private void jbtn_musteriEkleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_musteriEkleActionPerformed
       
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("BP2_1521221051_FeyzaNurSAKA_Odev2PU");
        EntityManager em = emf.createEntityManager();
        if (!jtxt_musteriAdi.getText().trim().equals("") && !jtxt_musteriSoyadi.getText().trim().equals("")) {

            Query q = em.createQuery("SELECT m from Musteri m");
            Query q2 = em.createQuery("SELECT m from Yonetici m");
            List<Musteri> musteriler = q.getResultList();
            List<Yonetici> yoneticiler=q2.getResultList();
            
            
            int maxid = 0;
            for (Musteri mus : musteriler) {
                if (mus.getMusteriid() > maxid) {
                    maxid = mus.getMusteriid();
                }
            }

            Musteri m = new Musteri();
            em.getTransaction().begin();
        
//            m.setYoneticiid(YoneticiGirisEkrani.y_logined.getYoneticiid());
          
            m.setMusteriadi(jtxt_musteriAdi.getText());
            m.setMusterisoyadi(jtxt_musteriSoyadi.getText());
            m.setMusteriid(maxid + 1);
            Random rdm = new Random();
            m.setMusterisifre(String.valueOf(rdm.nextInt(9999) + 1000));
            

            this.dtmmusterilistesi.addRow(new Object[]{m.getMusteriid(), m.getMusteriadi(), m.getMusterisoyadi()});
            jComboBoxmusteriid.addItem(m.getMusteriid());

            JOptionPane.showMessageDialog(rootPane, "Kayıt Başarılı. " + m.getMusteriadi() + " " + m.getMusterisoyadi() + " için belirlenen şifre " + m.getMusterisifre());
            em.persist(m);
            em.getTransaction().commit();
            em.close();
            emf.close();

            temizle();
            checkbox.setSelected(false);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Boşlukları Doldurunuz!");
            jbtn_musteriEkle.setEnabled(false);
            jbtn_musteriSil.setEnabled(false);

        }
    }//GEN-LAST:event_jbtn_musteriEkleActionPerformed

