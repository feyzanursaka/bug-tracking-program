    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        try {
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("BP2_1521221051_FeyzaNurSAKA_Odev2PU");
            EntityManager em = emf.createEntityManager();

            Query q = em.createQuery("SELECT k from Kayit k");
            List<Kayit> kayitlar = q.getResultList();
            int maxid = 0;
            for (Kayit kay : kayitlar) {
                if (kay.getKayitid() > maxid) {
                    maxid = kay.getKayitid();
                }
            }

            Kayit k = new Kayit();
            k.setMusteriid(Integer.parseInt("" + jComboBoxmusteriid.getSelectedItem()));
            k.setCihazdurumu("" + jComboCihazDurumu.getSelectedItem());
            k.setCihazturu("" + jComboCihazTuru.getSelectedItem());
            k.setTeslimtariihi(jtxt_TeslimTarihi.getText());
            k.setUcret(Integer.parseInt("" + jtxt_Ucret.getText()));
            k.setKayitid(maxid + 1);

            em.getTransaction().begin();
            em.persist(k);
            em.getTransaction().commit();

            Query q2 = em.createQuery("select k from Kayit k where k.musteriid=:mmusteriid");
            q2.setParameter("mmusteriid", Integer.parseInt("" + jComboBoxmusteriid.getSelectedItem()));
            List<Kayit> kayitlar2 = q2.getResultList();
            
            
            DefaultTableModel dtmk = new DefaultTableModel();
            dtmk.setColumnIdentifiers(new String[]{"MÜŞTERİ ID", "KAYIT ID", "TÜRÜ", "DURMU", "TARİH", "ÜCRET"});
            for (Kayit ka : kayitlar2) {

                dtmk.addRow(new Object[]{ka.getMusteriid(), ka.getKayitid(), ka.getCihazturu(), ka.getCihazdurumu(), ka.getTeslimtariihi(), ka.getUcret()});
            }
            em.close();
            emf.close();
            jButton1.setEnabled(true);
            jButton3.setEnabled(true);

            jtableurunlistesi.setModel(dtmk);

            JOptionPane.showMessageDialog(rootPane, "Kayıt Başarılı!");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Bilgileri Kontrol Ediniz!");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

