    private void jComboBox1İtemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1İtemStateChanged
        // TODO add your handling code here:
        if (jComboBox1.getSelectedIndex() == -1) {
            return;
        }
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("BP2_1521221051_FeyzaNurSAKA_Odev2PU");
        EntityManager em = emf.createEntityManager();
        Query q = em.createQuery("select k from Kayit k where k.kayitid=:kkayitid");
        q.setParameter("kkayitid", Integer.parseInt("" + jComboBox1.getSelectedItem()));
        List<Kayit> kayitlar = q.getResultList();
        DefaultTableModel dtmk = new DefaultTableModel();
        dtmk.setColumnIdentifiers(new String[]{"KAYIT ID", "TÜRÜ", "DURMU","TARİH", "ÜCRET"});
        for (Kayit k : kayitlar) {
            
            dtmk.addRow(new Object[]{k.getKayitid(), k.getCihazturu(), k.getCihazdurumu(), k.getTeslimtariihi(), k.getUcret()});
            jLabel1.setText(k.getUcret()+"");
        }
        jtablekayitlar.setModel(dtmk);
        em.close();
        emf.close();
        
    }//GEN-LAST:event_jComboBox1İtemStateChanged

