    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.setColumnIdentifiers(new String[]{"KAYIT ID", "TÜRÜ", "DURMU","TARİH", "ÜCRET"});
        jComboBox1.removeAllItems();
        try {
            boolean b = true;
            
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("BP2_1521221051_FeyzaNurSAKA_Odev2PU");
            EntityManager em = emf.createEntityManager();
            Query q = em.createQuery("select k from Kayit k where k.musteriid=:kmusteriId");
            
            q.setParameter("kmusteriId", Integer.parseInt(jtxtmusteriId.getText()));
            List<Kayit> kayitlar = q.getResultList();
            int toplam=0;
            for (Kayit k : kayitlar) {
                
                if (k.getMusteriid().equals(Integer.parseInt(jtxtmusteriId.getText()))) {
                    
                    dtm.addRow(new Object[]{k.getKayitid(), k.getCihazturu(), k.getCihazdurumu(), k.getTeslimtariihi(), k.getUcret()});
                    toplam+=k.getUcret();
                    
                    jComboBox1.addItem("" + k.getKayitid());
                    jtablekayitlar.setModel(dtm);
                    b = false;
                }
                
            }
            jLabel1.setText(""+toplam);
            if (b == true) {
                JOptionPane.showMessageDialog(rootPane, "Bu müşteriye ait kayıt bulunamamakta! ");
                temizle();
            }
            
        } catch (NumberFormatException e) {
             JOptionPane.showMessageDialog(rootPane, "Müşteri Id yanlış. Lütfen tekrar deneyiniz! ");
             temizle();
        }
        

    }//GEN-LAST:event_jButton1ActionPerformed
